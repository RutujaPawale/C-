#include<iostream>
using namespace std;

int a=10, b=5;
int main()
{
    cout<<"a + b = "<<a+b<<endl;
    cout<<"a - b = "<<a-b<<endl;
    cout<<"a * b = "<<a*b<<endl;
    cout<<"a / b = "<<a/b<<endl;
    cout<<"a++ = "<<a++<<endl;
    cout<<"a-- = "<<a--<<endl;
    cout<<"++a = "<<++a<<endl;
    cout<<"--a = "<<--a<<endl;
    return 0;
}
